import 'package:kikikaikai/core/models/content.dart';
import 'package:kikikaikai/core/models/content_type.dart';
import 'package:kikikaikai/data/dummy/dummy_contents.dart';

/// 音声コンテンツ ID → 書き起こし本文
final dummyTranscriptsByContentId = {
  for (final content in dummyContents)
    if (content.type.hasTranscriptTab)
      content.id: _transcriptFor(content),
};

String _transcriptFor(Content content) {
  final label =
      content.type == ContentType.kikikaikai ? '奇奇怪怪' : '団地ラジオ';
  final subtitle = content.cardSubtitle ?? '感想を取り戻す';

  return '''
TaiTan：${content.title}、$labelの本日の回です。まずは近況から入ります。

玉置：近況というより、最近の違和感の方が正確かもしれない。

TaiTan：SNSを見すぎると、自分の言葉が薄くなる感覚がある。情報は増えるのに、言いたいことが減る。

玉置：それは消費の問題だ。読むだけでは言葉は残らない。声に出すか、誰かに話すか、どちらかが要る。

TaiTan：朝、通勤中にタイムラインを見て、なぜか疲れてしまう。内容は覚えていないのに、気分だけが重い。

玉置：それは情報の量ではなく、速度の問題だ。頭の中で整理する前に次が流れてくる。整理できないまま一日が始まる。

TaiTan：だからラジオを聴く。画面を見なくていい。一つの声だけが続く。

玉置：一つの声に集中できる時間は、今の生活では贅沢品だ。短くてもいい。長く聴けなくてもいい。途中で止めてもいい。

TaiTan：今日の本編では「$subtitle」という話をした。考察を積み上げる前に、まず温度を拾う。

玉置：温度という言葉が好きだ。正しさより先に、触れたかどうか。触れたものだけが残る。

TaiTan：本編の前半では、最近読んだ本の話から入った。著者が書いているのは結論ではなく、迷い方の記録だった。

玉置：迷い方の記録——それは良い表現だ。答えを出す本より、問いを持ち帰る本の方が、後から効いてくる。

TaiTan：Listenerから「最近、何を見ても同じに感じる」というメッセージが来ていた。共感できる。

玉置：同じに感じるのは、感覚が鈍ったからではない。比較対象が増えすぎたからだ。比較を減らすと、輪郭が戻る。

TaiTan：比較を減らす方法はあるか。SNSをやめるのは一つの手だが、全部断つと情報の入口も狭まる。

玉置：断つ必要はない。入口を一つに絞るだけでいい。朝はニュース、夜は長文——時間帯で役割を分ける。

TaiTan：本編の中盤では、子どもの頃に聴いていたラジオの話をした。深夜、小さな音量で流していた声。

玉置：記憶に残るのは内容ではなく、部屋の暗さと声の距離感だ。誰かが隣の部屋で話しているような、不思議な近さ。

TaiTan：今の配信はいつでも聴ける。便利だが、あの「今しか聴けない」感覚は薄れた。

玉置：録音は時間を折りたたむ技術だ。ただ、折りたたまれた時間は、生の時間とは温度が違う。両方あっていい。

TaiTan：後半では「言葉を失った夜」の話。書こうとして、白い画面の前で手が止まる。

玉置：止まるのは、言葉がないからではない。言葉が多すぎて選べないからだ。一つだけ選べば動き出す。

TaiTan：一つだけ、というのは難しい。全部言いたい気持ちと、何も言えない気持ちが同時にある。

玉置：では、今この瞬間に浮かんだ単語を一つ書け。それが起点になる。文章は後からついてくる。

TaiTan：試しに書いた。単語は「余白」。何も書いていない時間の方が、最近は長く感じる。

玉置：余白は空白ではない。次の言葉が来るための置き場だ。詰め込みすぎると、音が濁る。

TaiTan：本編の締めに近い部分で、来週の予告の話をした。次回は「習慣の終わり方」について。

玉置：習慣を始める方法はよく語られる。終わらせ方は語られない。終わらせ方にも作法がある。

TaiTan：書き起こしを読むと、本編より雑談の方が生きている回も多い。今日もそうだった。

玉置：本編は骨格、雑談は肉付けだ。肉付けの方が体温が高いのは自然だ。体温が高い部分を残しておけ。

TaiTan：収録後、スタジオを出る前に一つだけメモを残した。「比較を減らす」。明日の朝、それだけ思い出す。

玉置：一つで十分だ。多く覚えようとすると、結局どれも薄くなる。

TaiTan：最後にひとつ。言葉を失ったと感じる夜こそ、ラジオは役に立つ。誰かの声を聞くことで、自分の声が戻ってくる。

玉置：戻ってくる声は、元と同じとは限らない。少し低くなっていたり、節々が欠けていたりする。それでも声であればいい。

TaiTan：今日も最後まで聴いてくれてありがとう。書き起こしを読んでくれた方も、同じく。

玉置：お疲れさまでした。次回も、余白を残して終わろう。
''';
}
